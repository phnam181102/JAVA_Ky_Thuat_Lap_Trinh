package Practices01;
//cau truc switch-case
public class Video12 {
    public static void main(String[] args) {
        int a = 3;
        switch (a){
            case 1: {
                System.out.println("a = 1");
                break;
            }
            case 2: {
                System.out.println("a = 2");
                break;
            }
            case 3: {
                System.out.println("a = 3");
                break;
            }
        }
    }
}
